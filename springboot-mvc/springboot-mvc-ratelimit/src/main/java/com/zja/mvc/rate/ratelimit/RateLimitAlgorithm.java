package com.zja.mvc.rate.ratelimit;

/**
 * 限流算法类型
 *
 * @author: zhengja
 * @since: 2024/03/11
 */
public enum RateLimitAlgorithm {

    /**
     * 固定窗口算法：在固定时间窗口内统计请求次数，超出限制则拒绝。
     * 优点：实现简单，内存占用少。
     * 缺点：存在窗口边界突刺问题。
     */
    FIXED_WINDOW,

    /**
     * 滑动窗口算法：维护一个滑动时间窗口，精确统计窗口内的请求次数。
     * 优点：解决了固定窗口的边界突刺问题，更平滑。
     * 缺点：内存占用略高。
     */
    SLIDING_WINDOW,

    /**
     * 令牌桶算法：以固定速率向桶中添加令牌，请求消耗令牌，桶满时多余令牌丢弃。
     * 优点：允许突发流量（消耗桶中积累的令牌），适合处理短时高并发。
     * 缺点：实现相对复杂。
     */
    TOKEN_BUCKET,

    /**
     * 漏桶算法：请求进入漏桶，以固定速率流出处理，超出桶容量则直接拒绝。
     * 优点：输出速率恒定，保护下游服务。
     * 缺点：无法应对突发流量（没有令牌桶灵活）。
     */
    LEAKY_BUCKET
}
